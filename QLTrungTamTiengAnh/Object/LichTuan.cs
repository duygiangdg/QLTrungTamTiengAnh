using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace QLTrungTamTiengAnh.Object
{
    class LichTuan
    {
        private string maPhong, maLop, thu, maLich;
        private int ca;

        public string MaPhong
        {
            get
            {
                return maPhong;
            }

            set
            {
                maPhong = value;
            }
        }

        public string MaLop
        {
            get
            {
                return maLop;
            }

            set
            {
                maLop = value;
            }
        }

        public string Thu
        {
            get
            {
                return thu;
            }

            set
            {
                thu = value;
            }
        }

        public int Ca
        {
            get
            {
                return ca;
            }

            set
            {
                ca = value;
            }
        }

        public string MaLich
        {
            get
            {
                return maLich;
            }

            set
            {
                maLich = value;
            }
        }
    }
}
